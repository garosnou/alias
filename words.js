function getWordsForGame(category, count) {
    const categoryData = WORDS_DATABASE[category];
    if (!categoryData) {
        return Array(count).fill("СЛОВО");
    }
    
    // Объединяем все уровни сложности
    const allWords = [];
    if (categoryData.easy) allWords.push(...categoryData.easy);
    if (categoryData.medium) allWords.push(...categoryData.medium);
    if (categoryData.hard) allWords.push(...categoryData.hard);
    
    if (allWords.length === 0) {
        return Array(count).fill("СЛОВО");
    }
    
    const shuffled = [...allWords].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, Math.min(count, allWords.length));
}

// Экспорт функций для использования в основном скрипте
window.WORDS_DATABASE = WORDS_DATABASE;
window.getRandomWord = getRandomWord;
window.getWordsForGame = getWordsForGame;
